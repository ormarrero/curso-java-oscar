package com.example.classcustomer;

public class Customer {

    String name;
    String email;
    Integer age;
    double points;

    public Customer(){

    }

    public Customer(String name, String email, Integer age, double points) {
        this.name = name;
        this.email = email;
        this.age = age;
        this.points = points;
    }
}
